public class TaiKhoan {
    public String maTK;
    public String hoTenTK;
    public String dienThoai;
    public String matKhau;

    public TaiKhoan() {
    }

    public TaiKhoan(String maTK,  String hoTenTK, String dienThoai, String matKhau) {
        this.maTK = maTK;
        this.hoTenTK = hoTenTK;
        this.dienThoai = dienThoai;
        this.matKhau = matKhau;
    }
}
