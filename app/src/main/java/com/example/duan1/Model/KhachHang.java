package com.example.duan1.Model;

public class KhachHang {
    public int maKH;
    public String tenKH;
    public String namSinh;
    public String dienThoai;
    public String diaChi;

    public KhachHang() {
    }

    public KhachHang(int maKH, String tenKH, String namSinh, String dienThoai, String diaChi) {
        this.maKH = maKH;
        this.tenKH = tenKH;
        this.namSinh = namSinh;
        this.dienThoai = dienThoai;
        this.diaChi = diaChi;
    }
}
