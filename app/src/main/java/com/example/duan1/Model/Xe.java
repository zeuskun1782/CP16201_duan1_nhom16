package com.example.duan1.Model;

public class Xe {
    public int maXe;
    public String tenXe;
    public int giaXe;
    public String mauXe;
    public String trangThai;
    public int maHang;

    public Xe() {
    }

    public Xe(int maXe, String tenXe, int giaXe, String mauXe, String trangThai, int maHang) {
        this.maXe = maXe;
        this.tenXe = tenXe;
        this.giaXe = giaXe;
        this.mauXe = mauXe;
        this.trangThai = trangThai;
        this.maHang = maHang;
    }
}
